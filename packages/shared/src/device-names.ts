/** Generate a random device name in adjective-adjective-noun format. */
export function generateDeviceName(): string {
  const bytes = new Uint8Array(3);
  crypto.getRandomValues(bytes);
  const adj1 = ADJECTIVES[bytes[0]! % ADJECTIVES.length];
  const adj2 = ADJECTIVES[bytes[1]! % ADJECTIVES.length];
  const noun = NOUNS[bytes[2]! % NOUNS.length];
  return `${adj1}-${adj2}-${noun}`;
}

/** Validate a device name matches the expected format. */
export function isValidDeviceName(name: string): boolean {
  const parts = name.split('-');
  if (parts.length !== 3) return false;
  return parts.every((p) => /^[a-z]+$/.test(p ?? ''));
}

